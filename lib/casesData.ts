export function getSimilarCases(
  cancerType: string,
  stage: string,
  ageGroup: string,
  subtype?: string
): Array<Case & { similarityScore: number; matchReasons: string[] }> {
  const scored = cases.map((c) => {
    let score = 0;
    const reasons: string[] = [];

    // Cancer type match (40 points)
    if (c.cancerType === cancerType) {
      score += 40;
      reasons.push(`Same cancer type (${c.cancerType === "hodgkin" ? "Hodgkin's Lymphoma" : c.cancerType.toUpperCase()})`);
    }

    // Stage match (30 points)
    const normalizedInputStage = stage.replace("Stage ", "").trim();
    const normalizedCaseStage = c.stage.replace("Stage ", "").trim();

    if (normalizedCaseStage === normalizedInputStage) {
      score += 30;
      reasons.push(`Exact same stage (${c.stage})`);
    } else if (
      normalizedCaseStage.charAt(0) === normalizedInputStage.charAt(0)
    ) {
      score += 18;
      reasons.push(`Similar stage group (${c.stage} vs ${stage})`);
    } else if (
      Math.abs(
        parseInt(normalizedCaseStage.charAt(0)) -
          parseInt(normalizedInputStage.charAt(0))
      ) === 1
    ) {
      score += 8;
      reasons.push(`Adjacent stage (${c.stage})`);
    }

    // Age group match (20 points)
    if (c.ageGroup === ageGroup) {
      score += 20;
      reasons.push(`Same age group (${ageGroup})`);
    } else if (
      (ageGroup === "child" && c.ageGroup === "teen") ||
      (ageGroup === "teen" && c.ageGroup === "child")
    ) {
      score += 10;
      reasons.push("Similar pediatric age range");
    }

    // Subtype match (10 points)
    if (subtype && c.subtype !== "N/A") {
      if (c.subtype === subtype) {
        score += 10;
        reasons.push(`Same subtype (Type ${subtype} — no B-symptoms)`);
      }
    }

    return { ...c, similarityScore: score, matchReasons: reasons };
  });

  return scored
    .filter((c) => c.similarityScore >= 40) // must at least match cancer type
    .sort((a, b) => b.similarityScore - a.similarityScore)
    .slice(0, 3);
}
